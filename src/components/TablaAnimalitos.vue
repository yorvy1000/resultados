  <script>
  export default {
    data() {
      return {
        currentIndex: 0,
        totalSlides: 5,
        intervalId: null,
        currentDate: ''
      };
    },
    mounted() {
      this.getCurrentDate();
      this.startCarousel();
    },
    methods: {
      getCurrentDate() {
        const options = { timeZone: 'America/Caracas', year: 'numeric', month: '2-digit', day: '2-digit' };
        this.currentDate = new Date().toLocaleDateString('es-VE', options);
      },
      startCarousel() {
        this.intervalId = setInterval(this.nextSlide, 5000);
      },
      stopCarousel() {
        clearInterval(this.intervalId);
      },
      nextSlide() {
        this.currentIndex = (this.currentIndex + 1) % this.totalSlides;
      },
      prevSlide() {
        this.currentIndex = (this.currentIndex - 1 + this.totalSlides) % this.totalSlides;
      }
    },
    beforeUnmount() {
      this.stopCarousel();
    }
  };
  </script>
  
  
